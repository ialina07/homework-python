def exchange(coins, target_sum):
    """
    Функция для размена суммы target_sum с использованием монет из списка coins
    Возвращает список монет для размена или '-42!', если размен невозможен
    """
    # Создаем массив для хранения минимального количества монет для каждой суммы от 0 до target_sum
    # Изначально все значения устанавливаем в бесконечность (недостижимое значение)
    min_coins = [float("inf")] * (target_sum + 1)

    # Базовый случай: для суммы 0 нужно 0 монет
    min_coins[0] = 0

    # Заполняем массив min_coins для всех сумм от 1 до target_sum
    for i in range(1, target_sum + 1):
        # Перебираем все доступные монеты
        for coin in coins:
            # Если монета не превышает текущую сумму i
            if coin <= i:
                # Обновляем минимальное количество монет для суммы i:
                # Берем минимум между текущим значением и
                # значением для суммы (i - coin) + 1 монета
                min_coins[i] = min(min_coins[i], min_coins[i - coin] + 1)

    # Если для суммы target_sum не найдено решение (значение осталось бесконечностью)
    if min_coins[target_sum] == float("inf"):
        # Возвращаем специальное значение, означающее невозможность размена
        return ("-42!")

    # Восстанавливаем конкретные монеты, используемые для размена
    res = []  # Список для хранения результата
    i = target_sum  # Начинаем с целевой суммы

    # Проходим от суммы target_sum обратно к 0, восстанавливая использованные монеты
    while i > 0:
        # Перебираем все монеты, чтобы найти, какая была использована
        for coin in coins:
            # Проверяем, соответствует ли текущее количество монет
            # количеству для суммы (i - coin) + 1
            if min_coins[i] == min_coins[i - coin] + 1:
                # Нашли монету, добавляем ее в результат
                res.append(coin)
                # Уменьшаем текущую сумму на номинал монеты
                i = i - coin
                # Прерываем внутренний цикл, переходим к следующей итерации
                break
    return res  # Возвращаем список монет для размена


# Основная часть программы
coins = [5, 7, 10]  # Доступные номиналы монет
target_sum = int(input("Введите сумму для размена: "))  # Считываем сумму размена с клавиатуры

# Вызываем функцию размена
res = exchange(coins, target_sum)

# Выводим результат
print("размен числа", target_sum, "монетами:", res)
